package kr.co.gildongmu.model.board.bean;

public class CommentBean {
	private String comm_name;
	private String comment_sqe;
	private String comm;
	private String sqe;
	
	public String getComm_name() {
		return comm_name;
	}
	public void setComm_name(String comm_name) {
		this.comm_name = comm_name;
	}
	public String getComment_sqe() {
		return comment_sqe;
	}
	public void setComment_sqe(String comment_sqe) {
		this.comment_sqe = comment_sqe;
	}
	public String getComm() {
		return comm;
	}
	public void setComm(String comm) {
		this.comm = comm;
	}
	public String getSqe() {
		return sqe;
	}
	public void setSqe(String sqe) {
		this.sqe = sqe;
	}
	
	
	
}
